# Program on if-else statements and loops

# If-else example
num = 7
if num % 2 == 0:
    print(num, "is Even")
else:
    print(num, "is Odd")

print("\nFor loop example:")
# For loop example
for i in range(1, 6):
    print("Number:", i)

print("\nWhile loop example:")
# While loop example
count = 1
while count <= 5:
    print("Count:", count)
    count += 1
